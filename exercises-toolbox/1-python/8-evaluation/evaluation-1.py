# Speichern von häufig genutzten Werten unter einem Namen
density_iron = 7.87
density_copper = 8.96

# Ausgabe beschreibt die ausgegebenen Werte besser
print("Berechnete Massen für Eisen:")
print(density_iron * 10.0)
print(density_iron * 16.7)
print(density_iron * 23.5)
print(density_iron * 28.5)
print(density_iron * 34.9)

print("Berechnete Massen für Kupfer:")
print(density_copper * 10.0)
print(density_copper * 19.7)
print(density_copper * 28.6)
print(density_copper * 39.4)
print(density_copper * 50.1)


